    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtidhabitacion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "debes seleccionar una habitación");
            txtidhabitacion.requestFocus();
            return;
        }
        if (txtidcliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "debes seleccionar un cliente");
            txtidcliente.requestFocus();
            return;
        }
        if (txtcosto.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "debes ingresar el costo de alojamiento");
            txtcosto.requestFocus();
            return;
        }

        vreserva datos = new vreserva();
        freserva funcion = new freserva();

        datos.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
        datos.setIdcliente(Integer.parseInt(txtidcliente.getText()));
        datos.setIdtrabajador(idUsuario);
        
        int seleccionado = cbotipo_reserva.getSelectedIndex();
        datos.setTipo_reserva((String) cbotipo_reserva.getItemAt(seleccionado));
        
        Calendar cal;
        int d,m,a;
        cal = dcfecha_reserva.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        datos.setFecha_reserva(new Date(d,m,a));
        
        cal = dcfecha_ingresa.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        datos.setFecha_ingresa(new Date(d,m,a));
        
        cal = dcfecha_salida.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        datos.setFecha_salida(new Date(d,m,a));
        
        datos.setCosto_alojamiento(Double.parseDouble(txtcosto.getText()));
        seleccionado = cboestado.getSelectedIndex();
        datos.setEstado((String) cboestado.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (funcion.insertar(datos)) {
                JOptionPane.showMessageDialog(rootPane, "la reserva fue registarado correctamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            datos.setIdreserva(Integer.parseInt(txtidreserva.getText()));
            datos.setIdtrabajador(Integer.parseInt(txtidtrabajador.getText()));

            if (funcion.editar(datos)) {
                JOptionPane.showMessageDialog(rootPane, "la reserva fue editado correctamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

