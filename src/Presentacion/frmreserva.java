    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidreserva.getText().equals("")) {
            int confirma = JOptionPane.showConfirmDialog(rootPane, "esta seguro de eliminar la reserva?", "confirmar", 2);

            if (confirma == 0) {
                freserva funcion = new freserva();
                vreserva datos = new vreserva();

                datos.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                funcion.eliminar(datos);
                mostrar("");
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

