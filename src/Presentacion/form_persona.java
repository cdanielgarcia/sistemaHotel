    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        persona.setIdpersona(Integer.parseInt(txtId.getText()));
        persona.setNombre(txtnombre.getText());
        persona.setApaterno(txtapaterno.getText());
        persona.setAmaterno(txtamaterno.getText());

        int seleccionado = cbotipo_documento.getSelectedIndex();
        persona.setTipo_documento((String) cbotipo_documento.getItemAt(seleccionado));
        persona.setNum_documento(txtnum_documento.getText());
        persona.setDireccion(txtdireccion.getText());
        persona.setTelefono(txttelefono.getText());
        persona.setEmail(txtemail.getText());
        if (personaModel.editar(persona)) {
            JOptionPane.showMessageDialog(null, "Registro Editado");
            mostrar();
        } else {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error inesperado");
        }


    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int id = Integer.parseInt(txtId.getText());
        if (personaModel.eliminar(id)) {
            JOptionPane.showMessageDialog(null, "Registro Eliminado");
            mostrar();
        } else {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error inesperado");

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

