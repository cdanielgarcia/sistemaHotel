    final void mostrar() {
        try {
            DefaultTableModel tabla = (DefaultTableModel) tablalistado.getModel();
            tabla.setColumnIdentifiers(new Object[]{
                "Id", "Documento", "Nombre", "Apellido", "Segundo apellido", "Direccion", "Telefono", "Email"
            });

            List<Persona> lista = personaModel.mostrar();
            tabla.setNumRows(0);
            for (int i = 0; i < lista.size(); i++) {
                Object[] row = {lista.get(i).getIdpersona(), lista.get(i).getNum_documento(), lista.get(i).getNombre(), lista.get(i).getApaterno(),
                    lista.get(i).getApaterno(), lista.get(i).getDireccion(),
                    lista.get(i).getTelefono(), lista.get(i).getEmail()};
                tabla.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane, e);
        }
    }

